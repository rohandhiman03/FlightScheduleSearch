/**
 * function to display the flights user had added as favourite
 */
    public void DisplayFavoriteFlights() {  

        DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();

        int rowstoremove = tblModel.getRowCount();

        for (int a = rowstoremove - 1; a >= 0; a--) {
            tblModel.removeRow(a);
        }
        /**
 * flights : getting the list of flights
 */
        ArrayList<FlightInfo> flights = m_searchRoutes.GetFavFlights(m_userId);  

        for (int i = 0; i < flights.size(); i++) {  
            /**
             * looping through the list and adding the flight details in the table
             */
            FlightInfo f = flights.get(i);

            String tabledata[] = {String.valueOf(f.getOutCarrier()), f.getOriginAirport(), f.getDestinationAirport(), Double.toString(f.getPrice()), f.getDepartureDate(), f.getReturnDate(), Integer.toString(f.getFlightID())};

            tblModel.addRow(tabledata);

        }

        int rowcount = tblModel.getRowCount();

        if (rowcount > 0) {  
            /**
             * checking if the atleast one value is present in the table else disabling the Delete Button
             */
            jButton1.setEnabled(true);
        } else {
            jButton1.setEnabled(false);
        }
    }

    /**
 * Function to delete the flights selected by the user in the Favourite table
 */    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    

        DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
        int rowcount = tblModel.getRowCount();
        int selected = 0;
        
        for(int i=0;i<rowcount;i++){  
            /**
             * checking if the user has selected any flight
             */
            if(tblModel.getValueAt(i, 7) != null && tblModel.getValueAt(i, 7).toString().trim().equals("true")){
                selected++;
            }
        }
        
        if(selected > 0){
        
        int response = JOptionPane.showConfirmDialog(this, "Do you want to delete the selected flights from your favourites", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);  //taking user confirmation to delete the selected flights
        if(response == JOptionPane.YES_OPTION){
        
        
        for (int i = rowcount-1; i >= 0; i--) {  
/**
 * looping through the selected values and deleting them one at a time
 */
            if (tblModel.getValueAt(i, 7) != null && tblModel.getValueAt(i, 7).toString().trim().equals("true")) {

                m_searchRoutes.DelFromFav(m_userId, Integer.parseInt(tblModel.getValueAt(i, 6).toString()));
                tblModel.removeRow(i);

            }
        }
        }
        }
        
        else{
            JOptionPane.showMessageDialog(this, "No Flights Selected To Be Deleted", "Flight API Delete Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

